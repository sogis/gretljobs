	*/
FROM 
	waldfunktion_waldnutzung_flaechen_berechnet AS wfn
LEFT JOIN waldflaechen_berechnet_plausibilisiert AS wfbp
	ON wfn.egrid = wfbp.egrid
GROUP BY 
	wfn.egrid,
	wfbp.flaeche
ORDER BY
	wfbp.flaeche DESC